    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        String srcTSRole = oldrole.getText();
        String modTSRole = newrole.getText();
        String modTSAmount = newamount.getText();

        if(srcTSRole.isEmpty() || modTSRole.isEmpty() || modTSAmount.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Role");
            return;
        }

        if(modTSRole.isEmpty()) {
            JOptionPane.showMessageDialog(this, "New Role cannot be empty");
            return;
        }

        if(!modTSAmount.chars().allMatch(Character::isDigit)
                || Integer.parseInt(modTSAmount) <=0) {
            JOptionPane.showMessageDialog(this, "New Amount cannot be empty");
            return;
        }


        TeamStruc updatedItem = new TeamStruc(modTSRole, Integer.parseInt(modTSAmount));

        boolean isUpdated = teamstrucsystem.update(srcTSRole, updatedItem);

        if (!isUpdated) {
            JOptionPane.showMessageDialog(this, "Property Owner name not found");
            return;
        }

        // Reset the modify fields
        oldrole.setText("");
        newrole.setText("");
        newamount.setText("");

        // Show confirmation pop up
        JOptionPane.showMessageDialog(this, "Item has been Modified");

        // Update display information
        performFileRelatedTask();
    }//GEN-LAST:event_UpdateActionPerformed


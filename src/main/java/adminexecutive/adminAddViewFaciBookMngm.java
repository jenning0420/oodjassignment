    private void faciBookingTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_faciBookingTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) faciBookingTable.getModel();
        int selectedInfo = faciBookingTable.getSelectedRow();

        facility.setText(model.getValueAt(selectedInfo, 0).toString());
        equipment.setText(model.getValueAt(selectedInfo, 1).toString());
        quantity.setText(model.getValueAt(selectedInfo, 2).toString());
        condition.setValue(model.getValueAt(selectedInfo, 3).toString());
    }//GEN-LAST:event_faciBookingTableMouseClicked

